     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tn = new News;
        $tn ->user_name = $request ->user_name; 
        $tn ->user_address = $request ->user_address; 

        $tn->save();

        return redirect() -> route('new.show');
    }

